# String Methods and String Slicing
my_string = "shivam shiv shiva shi shivammi"

print(len(my_string)) #calculates the length
print(my_string.capitalize()) #capitalizes the first letter
print(my_string.lower()) # makes everyletter lower case
print(my_string.upper()) # makes every letter upper case
print(my_string.title()) # makes first letter of every word uppercase
print(my_string.islower()) # tells if the string is lowercase
print(my_string.isupper()) # tells if every letter is uppercase
print(my_string.isalpha()) # tells if the string is alphabetical
print(my_string.isnumeric()) # tells if the string is numeric
print(my_string.isalnum()) #
print(my_string.isspace())
print(my_string.count("shiv"))
print(my_string.find("World"))
print(my_string.replace(" ", ","))
print(my_string.split(" "))
print(my_string.strip())
